#import "HMSColorElement.h"

@implementation HMSColorElement


+ (UIColor *) hms_darkRedColor
{
    return [UIColor colorWithRed:(109/255.0) green:(7/255.0) blue:(26/255.0) alpha:1.0];
}

+ (UIColor *) hms_grayColor
{
    return [UIColor colorWithRed:(253/255.0) green:(253/255.0) blue:(254/255.0) alpha:1.0];
}

+ (UIColor *) hms_lightRedColor
{
    return [UIColor colorWithRed:(145/255.0) green:(40/255.0) blue:(59/255.0) alpha:1.0];
}
@end
